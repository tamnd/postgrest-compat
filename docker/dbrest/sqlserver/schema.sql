-- SQL Server 2022 schema for the client compatibility test suite.
-- All tables live in the default dbo schema.
-- Engine notes:
--   boolean  → BIT              (0/1; coerced to bool by the result layer)
--   text[]   → NVARCHAR(MAX)    (JSON arrays; looksLikeJSON heuristic handles JSON)
--   serial   → INT IDENTITY(1,1)
--   UUID PK  → UNIQUEIDENTIFIER DEFAULT NEWID()
--   FTS      → SQL Server FULLTEXT catalog + FULLTEXT INDEX on todos.task
--
-- Named PRIMARY KEY constraints are required so the FULLTEXT INDEX creation can
-- reference the key index by a stable, known name.

-- Full-text catalog.
IF NOT EXISTS (SELECT 1 FROM sys.fulltext_catalogs WHERE name = N'CompatCatalog')
    CREATE FULLTEXT CATALOG CompatCatalog AS DEFAULT;
GO

-- todos
IF OBJECT_ID('dbo.todos', 'U') IS NULL
BEGIN
    CREATE TABLE dbo.todos (
        id   INT           NOT NULL IDENTITY(1,1),
        done BIT           NOT NULL DEFAULT 0,
        task NVARCHAR(500) NOT NULL,
        due  DATE          NULL,
        tags NVARCHAR(MAX) NOT NULL DEFAULT N'[]',
        CONSTRAINT PK_todos    PRIMARY KEY (id),
        CONSTRAINT UQ_todos_task UNIQUE (task)
    );
END;
GO

IF NOT EXISTS (
    SELECT 1 FROM sys.fulltext_indexes fi
    JOIN sys.objects o ON o.object_id = fi.object_id
    WHERE o.name = N'todos' AND o.type = 'U'
)
BEGIN
    CREATE FULLTEXT INDEX ON dbo.todos (task)
        KEY INDEX PK_todos ON CompatCatalog
        WITH CHANGE_TRACKING AUTO;
END;
GO

-- persons
IF OBJECT_ID('dbo.persons', 'U') IS NULL
CREATE TABLE dbo.persons (
    id    INT           NOT NULL IDENTITY(1,1),
    name  NVARCHAR(255) NOT NULL,
    age   INT           NULL,
    email NVARCHAR(255) NULL,
    CONSTRAINT PK_persons     PRIMARY KEY (id),
    CONSTRAINT UQ_persons_email UNIQUE (email)
);
GO

-- assignments
IF OBJECT_ID('dbo.assignments', 'U') IS NULL
CREATE TABLE dbo.assignments (
    id        INT NOT NULL IDENTITY(1,1),
    person_id INT NULL,
    todo_id   INT NULL,
    CONSTRAINT PK_assignments       PRIMARY KEY (id),
    CONSTRAINT UQ_assignments       UNIQUE (person_id, todo_id),
    CONSTRAINT FK_assign_person     FOREIGN KEY (person_id) REFERENCES dbo.persons(id) ON DELETE CASCADE,
    CONSTRAINT FK_assign_todo       FOREIGN KEY (todo_id)   REFERENCES dbo.todos(id)   ON DELETE CASCADE
);
GO

