-- RPC stored procedures (NativeRPC=true; discovered from sys.objects).
-- Scalar procs return a single column named after the proc so renderCall detects
-- them as scalar: len(cols)==1 && cols[0]==fnName heuristic.

IF OBJECT_ID('dbo.add', 'P') IS NOT NULL DROP PROCEDURE dbo.add;
GO
CREATE PROCEDURE dbo.add
    @a INT,
    @b INT
AS
    SELECT @a + @b AS [add];
GO

IF OBJECT_ID('dbo.get_todos_count', 'P') IS NOT NULL DROP PROCEDURE dbo.get_todos_count;
GO
CREATE PROCEDURE dbo.get_todos_count
AS
    SELECT CAST(COUNT(*) AS INT) AS [get_todos_count] FROM dbo.todos;
GO

IF OBJECT_ID('dbo.get_person_by_name', 'P') IS NOT NULL DROP PROCEDURE dbo.get_person_by_name;
GO
CREATE PROCEDURE dbo.get_person_by_name
    @name_param NVARCHAR(255)
AS
    SELECT id, name, age, email FROM dbo.persons WHERE name = @name_param;
GO
